from konlpy.tag import Okt
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

okt = Okt()

# =====================================
# 3. 발화 입력 (여기에 데이터 넣기)
# =====================================
utterances = [
    "새로운 문장입니다",
    "여기에 당신의 문장을 추가하세요"
]

# =====================================
# 4. 핵심 자동 분석 함수
# =====================================
def speech_analysis(utterances):
    results = []

    total_morphemes = 0
    total_words = 0
    vocab_set = set()

    utterance_lengths = []

    for utt in utterances:
        morphs = okt.morphs(utt)
        words = utt.split()

        total_morphemes += len(morphs)
        total_words += len(words)
        vocab_set.update(morphs)

        utterance_lengths.append(len(morphs))

        results.append({
            "발화": utt,
            "어절 수": len(words),
            "형태소 수": len(morphs)
        })

    df = pd.DataFrame(results)

    # ==============================
    # 5. 핵심 지표 자동 계산
    # ==============================
    MLU = total_morphemes / len(utterances)  # 평균 발화 길이 (형태소 기준)
    MLU_word = total_words / len(utterances) # 어절 기준 평균 발화 길이
    TTR = len(vocab_set) / total_morphemes   # 어휘 다양도

    summary = {
        "총 발화 수": len(utterances),
        "총 어절 수": total_words,
        "총 형태소 수": total_morphemes,
        "MLU (형태소 기준)": round(MLU, 2),
        "MLU (어절 기준)": round(MLU_word, 2),
        "TTR (어휘 다양도)": round(TTR, 2),
        "최대 발화 길이": max(utterance_lengths),
        "최소 발화 길이": min(utterance_lengths),
        "평균 발화 길이": round(np.mean(utterance_lengths), 2)
    }

    return df, summary, utterance_lengths

df, summary, lengths = speech_analysis(utterances)

# =====================================
# 6. 결과 출력
# =====================================
print("📊 발화별 분석 결과")
display(df)

print("\n📈 전체 자동 분석 지표")
for k, v in summary.items():
    print(f"{k}: {v}")

# =====================================
# 7. 시각화 (발화 길이 분포)
# =====================================
plt.figure(figsize=(8,4))
plt.bar(range(len(lengths)), lengths)
plt.xticks(range(len(lengths)), [f"U{i+1}" for i in range(len(lengths))])
plt.title("발화별 형태소 길이")
plt.ylabel("길이")
plt.show()

# =====================================
# 8. 치료용 자동 해석 (간단 피드백)
# =====================================
print("\n🧠 자동 해석")

if summary["MLU (형태소 기준)"] < 3:
    print("- 발화 길이가 짧은 편 → 문장 확장 훈련 필요")
elif summary["MLU (형태소 기준)"] < 5:
    print("- 중간 수준 발화 → 문장 구조 확장 가능")
else:
    print("- 비교적 긴 발화 → 문장 구성 양호")

if summary["TTR (어휘 다양도)"] < 0.4:
    print("- 어휘 다양도 낮음 → 다양한 단어 노출 필요")
else:
    print("- 어휘 다양도 양호")

print("\n✅ 분석 완료")

